    public void limpaCampos(){
    campoNome.setText("");
    campoEmail.setText("");
    campoCPF.setText("");
    campoSenha.setText("");
    campoTelefone.setText("");
    }
    
    private boolean camposObrigatoriosPreenchidos(){
    if(campoNome.getText().isEmpty() ||campoTelefone.getText().isEmpty() || campoEmail.getText().isEmpty() || campoCPF.getText().isEmpty() ||campoSenha.getText().isEmpty()){
        return false;
    }
    return true;    
    }
    

