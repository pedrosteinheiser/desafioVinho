    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed
      if(!camposObrigatoriosPreenchidos()){
          JOptionPane.showMessageDialog(null, "Os campos são de preenchimento obrigatório!");
      }
      
      else if(camposObrigatoriosPreenchidos()){
          owner.cadastraCliente(campoNome.getText(),campoEmail.getText(),campoCPF.getText(),campoTelefone.getText(),campoSenha.getText());
      }
    }//GEN-LAST:event_botaoCadastrarActionPerformed

